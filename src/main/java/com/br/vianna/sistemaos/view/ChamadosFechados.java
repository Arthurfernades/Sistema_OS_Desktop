    private void prencherTabela(ArrayList<OrdemServico> listaOS) {

        DefaultTableModel dftm = (DefaultTableModel) jtTabelaChamadoFechado.getModel();

        while (dftm.getRowCount() > 0) {
            dftm.removeRow(0);
        }

        for (OrdemServico os : listaOS) {

            String[] linha = {os.getProtocolo(), os.getCliente().getNome(), os.getDescricao(), formataDataString(os.getInicio()), formataDataString(os.getFim())};
            dftm.addRow(linha);

        }

    }

    private String formataDataString(Timestamp tempo) {

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        String formattedTimestamp = sdf.format(tempo);

        return formattedTimestamp;

    }

