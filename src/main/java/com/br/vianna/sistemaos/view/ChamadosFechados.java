    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {

            ArrayList<OrdemServico> listaOS = new OrdemServicoDAO().buscarTodos();

            listaOSCorreta = new ArrayList<>();

            for (OrdemServico os : listaOS) {

                if (os.getFim() != null) {

                    listaOSCorreta.add(os);

                }
            }

            prencherTabela(listaOSCorreta);

        } catch (ClassNotFoundException | SQLException ex) {

            JOptionPane.showMessageDialog(this, "Problema ao acessar o Banco de Dados");

        }
    }//GEN-LAST:event_formWindowOpened

    private void jbPesquisaProtocoloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisaProtocoloActionPerformed

        try {

            OrdemServico os = new OrdemServicoDAO().buscarPorProtocolo(jtPesquisaProtocolo.getText());

            DefaultTableModel dftm = (DefaultTableModel) jtTabelaChamadoFechado.getModel();

            while (dftm.getRowCount() > 0) {
                dftm.removeRow(0);
            }

            if (os.getFim() != null) {

                String[] linha = {os.getProtocolo(), os.getCliente().getNome(), os.getDescricao(), formataDataString(os.getInicio())};
                dftm.addRow(linha);
            }

        } catch (ClassNotFoundException | SQLException ex) {

            JOptionPane.showMessageDialog(this, "Protocolo não encontado!");

        }

    }//GEN-LAST:event_jbPesquisaProtocoloActionPerformed

    private void jbPesquisaCpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisaCpfActionPerformed

        try {

            Cliente cliente = new ClienteDAO().buscarPorCpf(jtPesquisaCpf.getText());

            listaOSCorreta = cliente.getListaOS();

            prencherTabela(listaOSCorreta);

        } catch (ClassNotFoundException | SQLException ex) {

            JOptionPane.showMessageDialog(this, "CPF não encontrado!");

        }

    }//GEN-LAST:event_jbPesquisaCpfActionPerformed

    private void jBConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConsultarActionPerformed

        int row = jtTabelaChamadoFechado.getSelectedRow();

        if (row <= -1) {

            JOptionPane.showMessageDialog(this, "Selecione um cliente!");

        } else {

            OSConsulta consulta = new OSConsulta(null, true);

            consulta.preparedEdit(listaOSCorreta.get(row));

            consulta.setVisible(true);

            formWindowOpened(null);

        }

    }//GEN-LAST:event_jBConsultarActionPerformed

