    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed

        int row = jtTabelaPesquisaCliente.getSelectedRow();

        if (row <= -1) {

            JOptionPane.showMessageDialog(this, "Selecione um cliente!");

        } else {

            CadastroCliente cadastro = new CadastroCliente(null, true);

            cadastro.preparedEdit(clientes.get(row));

            cadastro.setVisible(true);

            formWindowOpened(null);

        }
    }//GEN-LAST:event_jBAlterarActionPerformed

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed

        int row = jtTabelaPesquisaCliente.getSelectedRow();

        if (row <= -1) {

            JOptionPane.showMessageDialog(this, "Selecione um cliente!");

        } else {

            String senha = JOptionPane.showInputDialog("Senha mestra:");

            if (!senha.equals("123")) {

                JOptionPane.showMessageDialog(this, "Senha mestra incorreta!");

            } else {
                if (JOptionPane.showConfirmDialog(null, "Deseja apagar o cliente " + clientes.get(row).getNome() + "?",
                        "Apagar usuÃ¡rio", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                        cDao.delete(clientes.get(row));
                        JOptionPane.showMessageDialog(null, "Cliente apagado!");

                    formWindowOpened(null);
                }

            }
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

