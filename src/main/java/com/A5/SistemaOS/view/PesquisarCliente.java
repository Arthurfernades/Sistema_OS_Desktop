    private void jbPesquisaCpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisaCpfActionPerformed

            Cliente cliente = cDao.findByCpf(jtPesquisaCpf.getText());

            DefaultTableModel dftm = (DefaultTableModel) jtTabelaPesquisaCliente.getModel();

            while (dftm.getRowCount() > 0) {
                dftm.removeRow(0);
            }

            String[] linha = {cliente.getNome(), cliente.getCpf()};
            dftm.addRow(linha);

    }//GEN-LAST:event_jbPesquisaCpfActionPerformed

